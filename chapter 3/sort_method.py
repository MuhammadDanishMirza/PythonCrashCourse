list=['bmw', 'audi', 'toyota', 'subaru']
print(list)
list.sort()   # print the list in the forward alphabetical order (i.e a,b,c) and changes the original list permanently
print(list)

list.sort(reverse=True) 
print(list)   # print the list in the reverse alphabetical order (i.e z,y,x) and changes the original list permanently

